int	ft_iterative_factorial(int nb)
{
	int i;

	i = nb - 1;
	if (nb == 0 || nb == 1)
		return (1);
	if (nb < 0)
		return (0);
	while (0 < i)
	{
		nb = nb * i;
		i--;
	}
	return (nb);
}
